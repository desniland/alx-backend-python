#!/usr/bin/env python3
"""
This module that provides a function for adding two numbers.
"""


def add(a: float, b: float) -> float:
    """
    This is a function adds two numbers and returns the result.

    Parameters:
    a (float): The first number.
    b (float): The second number.

    Returns:
    float: The sum of a and b.
    """
    return a + b
